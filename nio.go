/*
Package nio implements modern and productive Go HTTP framework.

Example:

  package main

  import (
    "net/http"

    "github.com/go-nio/nio"
    "github.com/go-nio/nio/mw"
  )

  // Handler
  func hello(c nio.Context) error {
    return c.String(http.StatusOK, "Hello, World!")
  }

  func main() {
    // Nio instance
    n := nio.New()

    // Middleware
    n.Use(mw.Logger())
    n.Use(mw.Recover())

    // Routes
    n.GET("/", hello)

    // Start server
    n.Logger().Fatal(http.ListenAndServe(":1323", n))
  }

Learn more at https://github.com/go-nio/nio
*/
