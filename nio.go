type options struct {
	logger log.Logger
}

// A Option sets options such as credentials, tls, etc.
type Option func(*options)

// SetLogger allows to override default nio logger
func SetLogger(logger log.Logger) Option {
	return func(o *options) {
		o.logger = logger
	}
}

// New creates an instance of nio.
func New(opt ...Option) (e *Nio) {
	opts := options{
		logger: log.NewDefaultLogger(),
	}
	for _, o := range opt {
		o(&opts)
	}

	e = &Nio{
		maxParam: new(int),
		Binder:   &DefaultBinder{},
		logger:   opts.logger,
	}
	e.HTTPErrorHandler = e.DefaultHTTPErrorHandler
	e.pool.New = func() interface{} {
		return e.NewContext(nil, nil)
	}
	e.router = newRouter(e)
	return
}

// NewContext returns a Context instance.
func (e *Nio) NewContext(r *http.Request, w http.ResponseWriter) Context {
	return &context{
		request:  r,
		response: NewResponse(w),
		store:    make(map[string]interface{}),
		nio:      e,
		pvalues:  make([]string, *e.maxParam),
		handler:  NotFoundHandler,
	}
}

// DefaultHTTPErrorHandler is the default HTTP error handler. It sends a JSON response
// with status code.
func (e *Nio) DefaultHTTPErrorHandler(err error, c Context) {
	var (
		code = http.StatusInternalServerError
		msg  interface{}
	)

	if he, ok := err.(*HTTPError); ok {
		code = he.Code
		msg = he.Message
		if he.Internal != nil {
			err = fmt.Errorf("%v, %v", err, he.Internal)
		}
	} else if e.Debug {
		msg = err.Error()
	} else {
		msg = http.StatusText(code)
	}
	if _, ok := msg.(string); ok {
		msg = map[string]interface{}{"message": msg}
	}

	// Send response
	if !c.Response().Committed {
		if c.Request().Method == http.MethodHead { // Issue #608
			err = c.NoContent(code)
		} else {
			err = c.JSON(code, msg)
		}
		if err != nil {
			e.Logger().Error(err)
		}
	}
}

// Pre adds middleware to the chain which is run before router.
func (e *Nio) Pre(middleware ...MiddlewareFunc) {
	e.premiddleware = append(e.premiddleware, middleware...)
}

// Use adds middleware to the chain which is run after router.
func (e *Nio) Use(middleware ...MiddlewareFunc) {
	e.middleware = append(e.middleware, middleware...)
}

